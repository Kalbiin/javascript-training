// let lets you declare variables.
let age = 25;
let year = 2019;

// this shows into the console.
console.log(age, year);

// you can take the variable that's already been defined and change the value
age = 30;
console.log(age);

// const is like constant. We use this if we don't want a file to be overridden
const points = 100;
// cant change this "Uncaught TypeError" is the message that will show in the console log.
points = 50;
console.log(points);

// var (older way of creating variables)
var score = 75
console.log(score);
